    <div class="element clearfix col1-3 white auto whtmtn">
        <h4><strong>Challenge</strong></h4>
        <p>Being an international label originating in Japan required a design that was dynamic enough to be available in multiple languages.</p>
    </div>
    <div class="element clearfix col1-3 white auto whtmtn">
        <h4><strong>Solution</strong></h4>
        <p>The Issue was designed as an end of year annual and lookbook. This gave some additional direction when developing a color scheme and graphic identity. Using cartographics in a subdued manner reinforced both the rigidity and strenght of Mountains and the grid employed in the layout.</p>

    </div>
    <div class="element clearfix col1-3 white auto whtmtn">
        <h4><strong>Result</strong></h4>
        <p>This was a pet project for me, one that combined my love of layout design with my equally great admiration and appreciation of fashion. White Mountaineering is one of the worlds leading brands.</p>
